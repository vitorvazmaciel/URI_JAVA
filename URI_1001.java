public class URI_1001
{
    public static void main(String [] args)
    {
        java.util.Scanner tc = new java.util.Scanner(System.in);
    
        int a = tc.nextInt();
        int b = tc.nextInt();

        System.out.println("X = "+result(a, b));
    }
    
    static int result(int a, int b)
    {
        return a+b;
    }
}
